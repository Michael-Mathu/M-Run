  const Legend({
    required this.slug,
    required this.name,
    required this.country,
    required this.flag,
    required this.discipline,
    required this.tagline,
    required this.bio,
    required this.timeline,
    required this.records,
    required this.quotes,
    required this.emoji,
    required this.accent,
    this.beatLegendId,
  });
}

